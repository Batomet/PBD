EMPLOYEES.DEPARTMENT_ID = DEPARTMENTS.DEPARTMENT_ID JOIN LOCATIONS ON DEPARTMETS.LOCATION_ID = LOCATIONS.LOCATION_ID;

SELECT E1.LAST_NAME AS "pracownik", E2.LAST_NAME AS "manager" FRPM EMPLOYEES E1 JOIN EMPLOYEES E2 ON
E1.EMPLOYEE_ID = E2.MANAGER_ID WHERE E1.SALARY > 4000 AND E1.SALARY <7000 ORDER BY E1.LAST_NAME DESC;

SELECT E.LAST_NAME, J.JOB_TITLE, E.SALARY AS SALARY_PER_MONTH, J.MIN_SALARY, J.MAX_SALARY, D.DEPARTMENT_NAME FROM EMPLOYEES E
JOIN JOBS J ON J.JOB_ID = E.JOB_ID JOIN DEPARTMENTS D ON E.DEPARTMENT_ID = D.DEPRATMENT_ID WHERE J.JOB_TITLE = 'IT Helpdesk' ORDER BY E.SALARY DESC;
