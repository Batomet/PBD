SELECT REGIONS.REGION_NAME, COUNTRIES.COUNTRY_NAME FROM REGIONS JOIN COUNTRIES ON 
REGIONS.REGION_ID=COUNTIRES.REGION_ID ORDER BY REGIONS.REGION_ID ASC;

SELECT LOCATIONS.*, DEPARTAMENTS.DEPARTAMENT_NAME FROM LOCATIONS JOIN DEPARTMENTS ON 
DEPARTMENTS.LOCATION_ID=LOCATIONS.LOCATION_ID ORDER BY LOCATIONS.STATE_PROVINCE ASC;

SELECT EMPLOYEES.* FROM EMPLOYEES JOIN DEPARTMENTS ON 
DEPARTMENTS.DEPARTMENT_NAME LIKE 'Finance' JOIN JOBS ON EMPLOYEES.SALARY > (1.20 * JOBS.MIN_SALARY) AND EMPLOYEES.JOB_ID = JOBS.JOB_ID;

SELECT MAX(EEMPLOYEES.SALARY), '-' , MIN(EMPLOYEES.SALARY), '=', (MAX(EMPLOYEES.SALARY)-MIN(EMPLOYEES.SALARY))
FROM EMPLOYEES, DEPARTMENTS WHERE DEPARTMENTS.DEPARTMENT_NAME LIKE 'FINANCE';

SELECT EMPLOYEES.LAST_NAME FROM EMPLOYEES JOIN DEPARTMENS ON
EMPLOYEES.EMPLOYEE_ID = DEPARTMENTS.MANAGER_ID AND DEPARTMENTS.department_name = 'Human Resources';

SELECT EMPLOYEES.LAST_NAME, DEPARTMENTS.DEPARTMENT_NAME FORM EMPLOYEES JOIN
DEPARTMENTS ON EMPLYESS.DEPARTMENT_ID LIKE DEPARTMENTS.DEPARTMENT_ID JOIN JOBS ON JOBS.JOB_TITLE LIKE 'Sales Manager' AND EMPLOYEES,JOB_ID = JOBS.JOB_ID;

SELECT EMPLOYEES.LAST_NAME, REGIONS.REGION_NAME FROM EMPLOYEES JOIN DEPARTMENTS ON EMPLOYEES.DEPARTMENT_ID = DEPARTMENTS.DEPARTMENT_ID JOIN
LOCATIONS ON DEPARTMENTS.LOCATION_ID = LOCATIONS.LOCATION_ID JOIN COUNTRIES ON LOCATIONS.COUNTRY_ID JOIN REGIONS ON COUNTRIES.REGION_ID LIKE REGIONS.REGION_ID;

SELECT JOBS.JOB_TITLE, JOBS.MIN_SALARY FROM JOBS JOIN JOB_HISTOR ON JOB_HISTORY.JOB_ID
LIKE JOBS.JOB_ID JOIN EMPLOYEES ON JOB_HISTORY.EMPLOYEE_ID = EMPLOYEES.EMPLOYEE_ID AND EMPLYEES.LAST_NAME LIKE 'Hunold';

